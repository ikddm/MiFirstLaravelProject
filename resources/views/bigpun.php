        <a href="#"><h2>Big Pun</h2></a>
        <p>Big Punisher, también conocido como Big Pun, fue un rapero estadounidense de origen puertorriqueño. Nació el 10 de noviembre de 1971 y es conocido por su estilo lírico único.
        Nacido en una comunidad latina del Bronx, durante sus primeros años en el mundo del hip hop,3​ Christopher Rios practicaba basket, boxeo y otros deportes.3​ Conoció a su ahora esposa viuda Liza. A los 5 años se rompió una pierna en el Municipal Park de Manhattan. 10 años después, a los 15, Pun abandonó los estudios en Stevenson High School. Rios empezó a escribir para sus raps durante la década de los 80’ hasta el punto de formar Full A Clips Crew con Triple Seis, Cuban Link y Prospect. Después de asociarse en 1995 con el también originario de Puerto Rico y el Bronx Fat Joe, en 1995, 
        Pun hizo su debut comercial en Jealous Ones Envy (J.O.E.), tema del segundo álbum de Fat Joe.
        Después hizo público "I'm Not a Player" (sample de O'Jays) que triunfó en el mundo underground. El remix, "Still Not a Player" (con Joe), supuso el mayor éxito comercial del rapero latino. Su debut, Capital Punishment (1998) fue el primer álbum de un rapero latino en ser disco de platino.4​ Después de este éxito, se unió a “Terror Squad”, grupo de raperos latinos de Nueva York fundado por Fat Joe, quien constantemente se preocupó por el peso de Big Pun. Quería que Pun bajara de peso. Fue a Carolina del Norte y perdió</br>
